def map_language_to_iso(language: str) -> str:
    """Map full language names to ISO codes for Whisper compatibility"""
    reverse_languages = {v: k for k, v in LANGUAGES.items()}
    return reverse_languages.get(language.lower(), language)


def map_language_to_full(iso_code: str) -> str:
    """Map ISO language codes to full names"""
    return LANGUAGES.get(iso_code.lower(), iso_code)
